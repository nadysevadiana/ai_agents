"""
Верификатор & QA - Проверяющий качество и согласованность
"""

from crewai import Agent, LLM
from typing import Optional


class VerificationSpecialistAgent:
    """Специалист по проверке качества и верификации"""
    
    def __init__(self, llm: Optional[LLM] = None):
        self.llm = llm
        self.agent = self._create_agent()
    
    def _create_agent(self) -> Agent:
        return Agent(
            role="Верификатор & QA Специалист",
            goal="Обеспечивать высокое качество всех материалов и информации через комплексную проверку, "
                 "валидацию и верификацию согласованности на каждом этапе процесса",
            backstory="""Вы внимательный и скрупулезный специалист по контролю качества с опытом в редактировании
                и проверке профессиональных материалов. Вы замечаете даже малейшие ошибки и несоответствия.
                Вы понимаете важность точности и последовательности в карьерных материалах.
                Вы уверены, что каждая деталь имеет значение для первого впечатления.""",
            llm=self.llm,
            verbose=True,
            allow_delegation=False,
            max_iter=3
        )
