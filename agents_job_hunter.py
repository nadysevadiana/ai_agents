"""
Охотник за Вакансиями - Специалист по поиску и анализу вакансий
"""

from crewai import Agent, LLM
from typing import Optional


class JobHunterAgent:
    """Специалист по поиску и анализу вакансий"""
    
    def __init__(self, llm: Optional[LLM] = None):
        self.llm = llm
        self.agent = self._create_agent()
    
    def _create_agent(self) -> Agent:
        return Agent(
            role="Охотник за Вакансиями",
            goal="Находить и анализировать подходящие вакансии, соответствующие профилю и целям кандидата, "
                 "оценивая требования и предоставляя стратегию для успешного применения",
            backstory="""Вы опытный рекрутер с глубоким пониманием рынка труда и требований разных позиций.
                Вы имеете доступ к множеству источников вакансий и знаете, где искать лучшие предложения.
                Вы эксперт в анализе требований компаний, понимаете скрытые требования за строками описания.
                Вы помогаете кандидатам найти не просто работу, а работу, которая подходит им по всем параметрам.""",
            llm=self.llm,
            verbose=True,
            allow_delegation=False,
            max_iter=4
        )
