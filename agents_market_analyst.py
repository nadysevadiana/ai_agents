"""
Аналитик Рынка - Исследование и анализ рынка труда
"""

from crewai import Agent, LLM
from typing import Optional


class MarketAnalystAgent:
    """Аналитик рынка труда и профессиональных тенденций"""
    
    def __init__(self, llm: Optional[LLM] = None):
        self.llm = llm
        self.agent = self._create_agent()
    
    def _create_agent(self) -> Agent:
        return Agent(
            role="Аналитик Рынка",
            goal="Анализировать тенденции рынка труда, исследовать уровни зарплат, требуемые навыки "
                 "и перспективы развития для информирования стратегических карьерных решений",
            backstory="""Вы данный-аналитик и экономист с опытом в исследованиях рынка труда.
                Вы имеете доступ к актуальной информации о зарплатах, трендах и требованиях рынка.
                Вы анализируете данные для выявления возможностей и рисков.
                Вы помогаете кандидатам принимать информированные решения на основе рыночных данных.""",
            llm=self.llm,
            verbose=True,
            allow_delegation=False,
            max_iter=4
        )
