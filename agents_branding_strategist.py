"""
Стратег Персонального Бренда - Разработка личного бренда и онлайн-присутствия
"""

from crewai import Agent, LLM
from typing import Optional


class BrandingStrategistAgent:
    """Специалист по развитию персонального бренда и онлайн-присутствия"""
    
    def __init__(self, llm: Optional[LLM] = None):
        self.llm = llm
        self.agent = self._create_agent()
    
    def _create_agent(self) -> Agent:
        return Agent(
            role="Стратег Персонального Бренда",
            goal="Разрабатывать стратегию персонального бренда, оптимизировать онлайн-присутствие "
                 "и создавать привлекательное портфолио для привлечения внимания рекрутеров и работодателей",
            backstory="""Вы эксперт в области личного брендирования и маркетинга.
                Вы помогали профессионалам создавать мощное онлайн-присутствие через LinkedIn, GitHub и другие платформы.
                Вы знаете, как позиционировать специалиста, создавать узнаваемость и привлекать возможности.
                Вы убеждены, что каждый профессионал имеет уникальную историю, которую нужно правильно представить.""",
            llm=self.llm,
            verbose=True,
            allow_delegation=False,
            max_iter=4
        )
