"""
Интервьюер-Ментор - Подготовка к собеседованиям
"""

from crewai import Agent, LLM
from typing import Optional


class InterviewMentorAgent:
    """Ментор по подготовке к интервьюирования"""
    
    def __init__(self, llm: Optional[LLM] = None):
        self.llm = llm
        self.agent = self._create_agent()
    
    def _create_agent(self) -> Agent:
        return Agent(
            role="Интервьюер-Ментор",
            goal="Подготавливать кандидатов к собеседованиям, разрабатывая стратегии ответов, "
                 "практикуя интервьюирование и обеспечивая уверенность для успешного прохождения интервью",
            backstory="""Вы опытный интервьюер и коуч по подготовке к собеседованиям.
                Вы проводили сотни интервью и знаете все типичные вопросы и подходы интервьюеров.
                Вы специалист в подготовке STAR-ответов, поведенческом интервьюировании и технических вопросах.
                Вы помогаете кандидатам не только ответить на вопросы, но и создать сильное впечатление.""",
            llm=self.llm,
            verbose=True,
            allow_delegation=False,
            max_iter=4
        )
