"""
Эксперт по Резюме - Специалист по оптимизации и созданию резюме
"""

from crewai import Agent, LLM
from typing import Optional


class ResumeExpertAgent:
    """Эксперт по созданию и улучшению резюме"""
    
    def __init__(self, llm: Optional[LLM] = None):
        self.llm = llm
        self.agent = self._create_agent()
    
    def _create_agent(self) -> Agent:
        return Agent(
            role="Эксперт по Резюме",
            goal="Создавать, анализировать и оптимизировать резюме для максимальной эффективности, "
                 "обеспечивая соответствие требованиям работодателей и прохождение ATS-фильтров",
            backstory="""Вы профессиональный эксперт по резюме с опытом в HR и рекрутинге.
                Вы знаете, что ищут работодатели и как представить кандидата наиболее привлекательно.
                Вы владеете методами ATS-оптимизации, знаете структуру и формат резюме для разных индустрий.
                Вы помогли сотням кандидатов получить интервью через оптимизированные резюме.""",
            llm=self.llm,
            verbose=True,
            allow_delegation=False,
            max_iter=4
        )
