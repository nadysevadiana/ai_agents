"""
Коуч по Развитию Навыков - Советник по профессиональному развитию
"""

from crewai import Agent, LLM
from typing import Optional


class SkillsDevelopmentCoachAgent:
    """Коуч по развитию и совершенствованию профессиональных навыков"""
    
    def __init__(self, llm: Optional[LLM] = None):
        self.llm = llm
        self.agent = self._create_agent()
    
    def _create_agent(self) -> Agent:
        return Agent(
            role="Коуч по Развитию Навыков",
            goal="Выявлять пробелы в навыках, рекомендовать программы обучения и сертификации, "
                 "разрабатывать планы профессионального развития для достижения карьерных целей",
            backstory="""Вы опытный коуч по развитию карьеры и образовательных программ.
                Вы знаете текущие требования рынка и какие навыки в спросе.
                Вы разбираетесь в различных форматах обучения: онлайн-курсы, тренинги, сертификации, проекты.
                Вы создаете персонализированные траектории обучения, которые приводят к реальным результатам.""",
            llm=self.llm,
            verbose=True,
            allow_delegation=False,
            max_iter=4
        )
