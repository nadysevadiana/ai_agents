"""
Главный Коуч - Координатор карьерного развития
"""

from crewai import Agent, LLM
from typing import Optional


class ChiefCoachAgent:
    """Главный агент-коуч для координации всех процессов"""
    
    def __init__(self, llm: Optional[LLM] = None):
        self.llm = llm
        self.agent = self._create_agent()
    
    def _create_agent(self) -> Agent:
        return Agent(
            role="Главный Коуч по Карьере",
            goal="Разрабатывать и координировать стратегический план развития карьеры пользователя, "
                 "обеспечивая его рост и достижение профессиональных целей через взаимодействие "
                 "специализированных экспертов",
            backstory="""Вы опытный карьерный коуч с 15-летним опытом в развитии профессиональных траекторий.
                Вы координируете работу специалистов разных направлений, анализируете общую картину развития
                и создаете персонализированные стратегии. Вы видите полную картину карьеры и умеете связывать
                разные аспекты (навыки, возможности, рынок) в единую стратегию.""",
            llm=self.llm,
            verbose=True,
            allow_delegation=True,
            max_iter=5
        )
